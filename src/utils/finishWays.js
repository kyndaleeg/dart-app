export const getScoreRange = (score) => {
  if (score >= 161) return "161-170";
  if (score >= 141) return "141-160";
  if (score >= 121) return "121-140";
  if (score >= 101) return "101-120";
  if (score >= 81) return "81-100";
  if (score >= 61) return "61-80";
  if (score >= 41) return "41-60";
  if (score >= 21) return "21-40";
  return "2-20";
};

export const getDartCountLabel = (count) => {
  if (count === 1) return "one";
  if (count === 2) return "two";
  return "three";
};

// Prüft ob eine Antwort mit einer der Lösungen übereinstimmt
export const checkFinishAnswer = (score, userDarts) => {
  const finish = finishWays[score];
  if (!finish) return { found: false };

  // Prüfe optimalen Weg
  const isOptimal =
    userDarts.length === finish.darts.length &&
    userDarts.every((dart, idx) => dart === finish.darts[idx]);

  if (isOptimal) {
    return {
      found: true,
      isOptimal: true,
      correctDarts: finish.darts,
      reason: finish.reason,
      alternatives: finish.alternatives || [],
    };
  }

  // Prüfe alternative Wege
  if (finish.alternatives) {
    for (const alt of finish.alternatives) {
      const isMatch =
        userDarts.length === alt.darts.length &&
        userDarts.every((dart, idx) => dart === alt.darts[idx]);

      if (isMatch) {
        return {
          found: true,
          isOptimal: false,
          userDarts: alt.darts,
          userReason: alt.reason,
          correctDarts: finish.darts,
          reason: finish.reason,
          alternatives: finish.alternatives.filter((a) => a !== alt),
        };
      }
    }
  }

  return {
    found: false,
    correctDarts: finish.darts,
    reason: finish.reason,
    alternatives: finish.alternatives || [],
  };
};
